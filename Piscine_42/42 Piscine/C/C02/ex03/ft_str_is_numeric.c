int	ft_str_is_numeric(char *str)
{
	int	i;
	int	t;

	i = 0;
	t = 1;
	while (str[i] != '\0')
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
			t = 0;
		i++;
	}
	return (t);
}
