#include	<unistd.h>

void	fct_write(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb <= -2147483648)
	{
		write(1, &"-2147483648", 11);
		return (0);
	}
	if (nb < 0)
	{
		nb = nb * -1;
		fct_write('-');
	}
	if (nb > 9)
		ft_putnbr(nb / 10);
	fct_write(nb % 10 + '0');
}
