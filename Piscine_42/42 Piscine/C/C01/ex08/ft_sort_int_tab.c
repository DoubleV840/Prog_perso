void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (j < (size - 1))
	{
		if (tab[j] > tab[j + 1])
		{
			i = tab[j];
			tab[j] = tab[j + 1];
			tab[j + 1] = i;
			j = 0;
		}
		else
			j++;
	}
}
